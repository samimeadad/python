# \" - print the " in a string
# \' - print ' in a string
# \\ - print the \ in a string
# \n - print the new line in a string

myName = "Sami \"Meadad\" Choudhury"
print("My name is " + myName)

myAmma = "\'Aarishamma\'"
print(myAmma)

print("Thi Mama \\ Aarishamma")

print("Thi Mama\nAarishamma")
